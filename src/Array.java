public class Array {

    public static void main(String[] args){
        int [] array = {11,13,26,34,45,66}; // Index starts at 0
                     //  {0,1,2,3,4,5}
        int index = 0;
        while (index < 4) {
            System.out.println(array[index]);
            index++;
        }
    }
}
